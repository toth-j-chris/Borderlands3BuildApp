package app.model;

public class SkillTier {
    private int tierNum;
    private String tierDesc;

    public int getTierNum() {
        return tierNum;
    }

    public void setTierNum(int tierNum) {
        this.tierNum = tierNum;
    }

    public String getTierDesc() {
        return tierDesc;
    }

    public void setTierDesc(String tierDesc) {
        this.tierDesc = tierDesc;
    }

}